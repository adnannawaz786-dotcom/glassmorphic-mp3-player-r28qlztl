export const getTrackById = (id) => {
  return mockTracks.find(track => track.id === id);
};

export const getTracksByIds = (ids) => {
  return mockTracks.filter(track => ids.includes(track.id));
};

export const getTracksByArtist = (artist) => {
  return mockTracks.filter(track => track.artist === artist);
};

export const getTracksByGenre = (genre) => {
  return mockTracks.filter(track => track.genre === genre);
};

export const getFavoriteTracks = () => {
  return mockTracks.filter(track => track.favorite);
};

export const getRecentlyPlayed = () => {
  return mockTracks
    .sort((a, b) => new Date(b.addedDate) - new Date(a.addedDate))
    .slice(0, 6);
};

export const getMostPlayed = () => {
  return mockTracks
    .sort((a, b) => b.playCount - a.playCount)
    .slice(0, 6);
};

export const getPlaylistById = (id) => {
  return mockPlaylists.find(playlist => playlist.id === id);
};

export const getPlaylistTracks = (playlistId) => {
  const playlist = getPlaylistById(playlistId);
  if (!playlist) return [];
  return getTracksByIds(playlist.trackIds);
};

export const formatDuration = (seconds) => {
  const minutes = Math.floor(seconds / 60);
  const remainingSeconds = seconds % 60;
  return `${minutes}:${remainingSeconds.toString().padStart(2, '0')}`;
};

export const generateRandomWaveform = (length = 20) => {
  return Array.from({ length }, () => Math.random() * 0.8 + 0.1);
};